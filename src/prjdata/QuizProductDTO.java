package prjdata;

public class QuizProductDTO {
	private int product_Number = 0;
	private String product_Name = null;
	private int product_Price = 0;
	private String product_Image = null;
	private int product_Stock = 0;
	private String product_Company = null;
	private String product_Contents = null;
	
	
	public int getProduct_Number() {
		return product_Number;
	}
	public void setProduct_Number(int product_Number) {
		this.product_Number = product_Number;
	}
	public String getProduct_Name() {
		return product_Name;
	}
	public void setProduct_Name(String product_Name) {
		this.product_Name = product_Name;
	}
	public int getProduct_Price() {
		return product_Price;
	}
	public void setProduct_Price(int product_Price) {
		this.product_Price = product_Price;
	}
	public String getProduct_Image() {
		return product_Image;
	}
	public void setProduct_Image(String product_Image) {
		this.product_Image = product_Image;
	}
	public int getProduct_Stock() {
		return product_Stock;
	}
	public void setProduct_Stock(int product_Stock) {
		this.product_Stock = product_Stock;
	}
	public String getProduct_Company() {
		return product_Company;
	}
	public void setProduct_Company(String product_Company) {
		this.product_Company = product_Company;
	}
	public String getProduct_Contents() {
		return product_Contents;
	}
	public void setProduct_Contents(String product_Contents) {
		this.product_Contents = product_Contents;
	}	
}
